package hu.me.krz.haladojava;

public class Person {
	private final int age;
	private final String name;
	
	public Person(int age, String name) {
		super();
		this.age = age;
		this.name = name;
	}
	
	
	
	public int getAge() {
		return age;
	}
	
	public String getName() {
		return name;
	}
	
	
}
